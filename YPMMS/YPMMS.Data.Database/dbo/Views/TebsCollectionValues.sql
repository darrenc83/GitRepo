CREATE VIEW dbo.TebsCollectionValues
AS
SELECT        TOP (100) PERCENT s1.MachineId, s1.Tebscode, s1.KeyId, s1.Country, s1.Timestamp, s3.amount
FROM            dbo.TebsRecord AS s1 INNER JOIN
                             (SELECT        Tebscode, MAX(Timestamp) AS mts
                               FROM            dbo.TebsRecord
                               WHERE        (RecordType = 'BillCashboxRemoved')
                               GROUP BY Tebscode) AS s2 ON s2.Tebscode = s1.Tebscode AND s1.Timestamp = s2.mts LEFT OUTER JOIN
                             (SELECT        Tebscode, SUM(Value) AS amount
                               FROM            dbo.TebsRecord AS TebsRecord_1
                               WHERE        (RecordType = 'CashCredit') OR
                                                         (RecordType = 'AdjustmentCredit')
                               GROUP BY Tebscode) AS s3 ON s3.Tebscode = s2.Tebscode
ORDER BY s1.Timestamp DESC
GO
